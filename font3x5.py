        line1 = (((b1 >> 1) & 0xe) >> 1) & 7
        line2 = (((((b1 & 3) << 2) | (b2 & 2)) & 0xe) >> 1) & 7
        line3 = (((b2 >> 4) & 0xe) >> 1) & 7
        line4 = (((b2 >> 1) & 0xe) >> 1) & 7

